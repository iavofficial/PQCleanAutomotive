/***********************************************************************************************************************
* Name:        FsmSw_Kyber_Load32LittleEndian
*
* Description: load 4 bytes into a 32-bit integer
*              in little-endian order
*
* Arguments:   - const uint8 *x: pointer to input byte array
*
* Returns 32-bit unsigned integer loaded from x
***********************************************************************************************************************/
uint32 FsmSw_Kyber_Load32LittleEndian(const uint8 x[4])
{
  uint32 r = 0;

  r = (uint32)x[0];
  r |= (uint32)x[1] << 8u;
  r |= (uint32)x[2] << 16u;
  r |= (uint32)x[3] << 24u;

  return r;
}

/***********************************************************************************************************************
* Name:        FsmSw_Kyber_Cbd2
*
* Description: Given an array of uniformly random bytes, compute
*              polynomial with coefficients distributed according to
*              a centered binomial distribution with parameter eta=2
*
* Arguments:   -       poly  *r:   pointer to output polynomial
*              - const uint8 *buf: pointer to input byte array
***********************************************************************************************************************/
void FsmSw_Kyber_Cbd2(poly *r, const uint8 buf[2u * KYBER_N / 4u])
{
  uint8 i  = 0;
  uint8 j  = 0;
  uint32 t = 0;
  uint32 d = 0;
  sint16 a = 0;
  sint16 b = 0;

  for (i = 0; i < (KYBER_N / 8u); i++)
  {
    t = FsmSw_Kyber_Load32LittleEndian(&buf[4u * i]);
    d = t & 0x55555555u;
    d += (t >> 1u) & 0x55555555u;

    for (j = 0; j < 8u; j++)
    {
      a                       = (sint16)((uint16)((uint16)((d >> ((4u * j)))) & 0x3u));
      b                       = (sint16)((uint16)((uint16)((d >> ((4u * j) + 2u))) & 0x3u));
      r->coeffs[(8u * i) + j] = a - b;
    }
  }
}
